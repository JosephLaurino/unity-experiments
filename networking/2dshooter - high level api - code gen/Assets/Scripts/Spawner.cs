	void Start()
	{
		SpawnObstacles();
	}
	

	void SpawnAsteroids()
	{	
		for (int i = 0; i < amount; i++)
		{
			GameObject go = (GameObject)Instantiate(asteroidPrefab, new Vector3(Random.Range(-40, 40), Random.Range(-40,40)), Quaternion.identity);
			go.transform.localScale = new Vector3(4,4,4);
			go.GetComponent<Asteroid>().size = 4;

			float dx = (float)Random.Range(-40,40)/10.0f;
			float dy = (float)Random.Range(-40,40)/10.0f;
			float dir = (float)Random.Range(-40,40);
			go.transform.rotation = Quaternion.Euler(0, 0, dir);
			go.GetComponent<Rigidbody2D>().angularVelocity = dir;
			go.GetComponent<Rigidbody2D>().velocity = new Vector2(dx,dy);

			NetworkServer.Spawn(go);
		}
	}
	
	void SpawnObstacles()
	{
		int y=0;
		int x=0;
		for (int i=0; i < obstacles.Length; i++)
		{
			if (obstacles[i] == 1)
			{
				Instantiate(obstaclePrefab, new Vector3(-40 + x*2, -40 + y*2,0), Quaternion.identity);
			}
			if (i % 40 == 0) {
				y = y + 1;
				x = 0;
			}
			else
			{
				x = x + 1;
			}
		}
	}
	
	void Update ()
	{
		if (!NetworkServer.active) {
			return;
		}
		
		if (Powerup.numPowerups < amount*4)
		{
			Vector3 pos = new Vector3(Random.Range(-40, 40), Random.Range(-40,40), 0);
			var hits = Physics2D.OverlapCircleAll(pos, 2.0f);
			while (hits.Length > 0)
			{
				pos = new Vector3(Random.Range(-40, 40), Random.Range(-40,40), 0);
				hits = Physics2D.OverlapCircleAll(pos, 2.0f);
			}
			
			GameObject a = (GameObject)Instantiate(powerupPrefab, pos, Quaternion.identity);
			a.GetComponent<Powerup>().mbuf = (Buf.BufType)Random.Range(0,(int)Buf.BufType.Last);
			a.GetComponent<Powerup>().testMe = 1234;
			NetworkServer.Spawn(a);	
		}
		
		
		if (Asteroid.numAsteroids == 0)
		{
			//SpawnAsteroids();
		}
	}
